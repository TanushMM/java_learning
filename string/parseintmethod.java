package string;

public class parseintmethod {
    public static void main(String args[]) {
        // parseInt() belongs to Integer class
        String str = "123";
        int number = Integer.parseInt(str);
        System.out.println(number);
        
        String str1 = "456";
        number = Integer.parseInt(str1);
        System.out.println(number);
       
    }
 
}
